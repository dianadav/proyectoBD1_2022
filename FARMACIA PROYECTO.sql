--Paso 1: CREAR TABLAS Y AÑADIR CONSTRAINT

create table Laboratorio(
IdLaboratorio int PRIMARY KEY not null, 
Nombre varchar(200) not null,
CONSTRAINT UNIQUE_Laboratorio_Nombre unique (Nombre)
);

create table TipoMedicamento(
IdTipoMedicamento int PRIMARY KEY not null, 
Nombre varchar(200) not null,
CONSTRAINT UNIQUE_TipoMedicamento_Nombre unique (Nombre)
);

create table Categoria(
IdCategoria int PRIMARY KEY not null, 
Nombre varchar(200) not null, 
IdCategoriaPadre int,
CONSTRAINT UNIQUE_Categoria_Nombre unique (Nombre),
CONSTRAINT FK_Categoria_IdCategoriaPadre FOREIGN KEY (IdCategoriaPadre) references Categoria(IdCategoria)
);

create table Persona(
Dni varchar(15) PRIMARY KEY not null, 
Rtn varchar(16), 
Nombre varchar(50) not null, 
Apellido varchar(50) not null, 
Telefono varchar(15) not null, 
Ciudad varchar(100) not null, 
Ubicacion varchar(200) not null
);

create table Sucursal(
IdSucursal int PRIMARY KEY not null, 
Nombre varchar(200) not null, 
Telefono int not null, 
Ciudad varchar(100) not null, 
Ubicacion varchar(200) not null
);

create table Horario(
IdHorario int PRIMARY KEY not null, 
Dia varchar(2) not null, 
HoraApertura varchar(8) not null, 
HoraCierre varchar(8) not null,
CONSTRAINT CHK_Horario_Dia CHECK (Dia in ('Lu', 'Ma', 'Mi', 'Ju', 'Vi', 'Sa', 'Do'))
);

create table Medicamento(
IdMedicamento int PRIMARY KEY not null, 
Nombre varchar(200) not null, 
IdCategoria int not null,
CONSTRAINT UNIQUE_Medicamento_Nombre unique (Nombre),
CONSTRAINT FK_Medicamento_IdCategoria FOREIGN KEY (IdCategoria) references Categoria(IdCategoria)
);

create table LaboratorioProveeMedicamento(
IdLaboratorio int not null, 
IdMedicamento int not null,
CONSTRAINT PK_LaboratorioProveeMedicamento PRIMARY KEY (IdLaboratorio, IdMedicamento),
CONSTRAINT FK_LaboratorioProveeMedicamento_IdLaboratorio FOREIGN KEY (IdLaboratorio) references Laboratorio(IdLaboratorio),
CONSTRAINT FK_LaboratorioProveeMedicamento_IdMedicamento FOREIGN KEY (IdMedicamento) references Medicamento(IdMedicamento)
);

create table DetalleMedicamento(
IdDetalleMedicamento int PRIMARY KEY not null, 
Unidad varchar(200) not null, 
Cantidad int not null, 
Precio FLOAT not null, 
IdTipoMedicamento int not null, 
IdMedicamento int not null,
CONSTRAINT FK_DetalleMedicamento_IdTipoMedicamento FOREIGN KEY (IdTipoMedicamento) references TipoMedicamento(IdTipoMedicamento),
CONSTRAINT FK_DetalleMedicamento_IdMedicamento FOREIGN KEY (IdMedicamento) references Medicamento(IdMedicamento)
);

create table Inventario(
IdSucursal int not null, 
IdDetalleMedicamento int not null, 
FechaHoraRegistro TIMESTAMP not null, 
Cantidad int not null, 
FechaHoraModificacion TIMESTAMP not null, 
FechaCaducidad DATE not null,
CONSTRAINT PK_Inventario PRIMARY KEY (IdSucursal, IdDetalleMedicamento, FechaHoraRegistro),
CONSTRAINT FK_Inventario_IdSucursal FOREIGN KEY (IdSucursal) references Sucursal(IdSucursal),
CONSTRAINT FK_Inventario_IdDetalleMedicamento FOREIGN KEY (IdDetalleMedicamento) references DetalleMedicamento(IdDetalleMedicamento)
);


create table SucursalAbarcaHorario(
IdSucursal int not null, 
IdHorario int not null,
CONSTRAINT PK_SucursalAbarcaHorario PRIMARY KEY (IdSucursal, IdHorario),
CONSTRAINT FK_SucursalAbarcaHorario_IdSucursal FOREIGN KEY (IdSucursal) references Sucursal(IdSucursal),
CONSTRAINT FK_SucursalAbarcaHorario_IdHorario FOREIGN KEY (IdHorario) references Horario(IdHorario)
);

create table Cliente(
Dni varchar(15) PRIMARY KEY not null, 
FechaPrimeraCompra date not null,
CONSTRAINT FK_Cliente_Dni FOREIGN KEY (Dni) references Persona(Dni)
);

create table Empleado(
Dni varchar(15) PRIMARY KEY not null, 
Cargo varchar(200) not null, 
Profesion varchar(200) not null, 
DniSupervisor varchar(15), 
IdSucursal int not null,
CONSTRAINT CHK_Empleado_Cargo CHECK (Cargo in ('Cajero', 'Gerente general', 'Personal de limpieza', 'Gerente de sucursal', 'Seguridad')),
CONSTRAINT FK_Empleado_Dni FOREIGN KEY (Dni) references Persona(Dni),
CONSTRAINT FK_Empleado_DniSupervisor FOREIGN KEY (DniSupervisor) references Empleado(Dni),
CONSTRAINT FK_Empleado_IdSucursal FOREIGN KEY (IdSucursal) references Sucursal(IdSucursal)
);


create table Factura(
IdFactura int PRIMARY KEY not null, 
Subtotal FLOAT not null, 
total FLOAT not null, 
Impuesto FLOAT not null, 
Descuento FLOAT not null, 
Fecha date not null, 
TipoPago varchar(20) not null, 
DniCliente varchar(15) not null, 
DniEmpleado varchar(15) not null, 
IdSucursal int not null,
CONSTRAINT CHK_Factura_TipoPago CHECK (TipoPago in ('Efectivo', 'Tarjeta','Transferencia')),
CONSTRAINT FK_Factura_DniCliente FOREIGN KEY (DniCliente) references Cliente(Dni),
CONSTRAINT FK_Factura_DniEmpleado FOREIGN KEY (DniEmpleado) references Empleado(Dni),
CONSTRAINT FK_Factura_IdSucursal FOREIGN KEY (IdSucursal) references Sucursal(IdSucursal)
);


create table CorrespondeFactura(
IdDetalleMedicamento int not null, 
IdFactura int not null, 
Cantidad int not null,
CONSTRAINT PK_CorrespondeFactura PRIMARY KEY (IdDetalleMedicamento, IdFactura),
CONSTRAINT FK_CorrespondeFactura_IdDetalleMedicamento FOREIGN KEY (IdDetalleMedicamento) references DetalleMedicamento(IdDetalleMedicamento),
CONSTRAINT FK_CorrespondeFactura_IdFactura FOREIGN KEY (IdFactura) references Factura(IdFactura)
);



